        } else if (i < length(parts)) {
        new_text <- paste(new_text, ";", sep = "")
        }
    }
    new_text
    }

    clean_column_names <- function(df) {
    names(df) <- gsub(" ", "_", names(df))
    return(df)
    }

    dbcan_raw <- read.csv(input_csv, header = TRUE,check.names=FALSE)
    dbcan_raw<- clean_column_names(dbcan_raw)

